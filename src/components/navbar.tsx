export default function Navbar() {
  const pathname = usePathname()
  const router = useRouter()
  const [user, setUser] = useState<User | null>(null)
  const [open, setOpen] = useState(true)

  useEffect(() => {
    const saved = localStorage.getItem("sidebar_open")
    if (saved !== null) setOpen(saved === "true")
  }, [])

  function toggleOpen() {
    setOpen(prev => {
      localStorage.setItem("sidebar_open", String(!prev))
      return !prev
    })
  }

  useEffect(() => {
    const supabase = createClient()
    supabase.auth.getUser().then(({ data }) => setUser(data.user))
    const { data: listener } = supabase.auth.onAuthStateChange((_event, session) => {
      setUser(session?.user ?? null)
    })
    return () => listener.subscription.unsubscribe()
  }, [])

  async function handleLogout() {
    const supabase = createClient()
    await supabase.auth.signOut()
    router.push("/")
    router.refresh()
  }

  // Verberg sidebar op landing page en login
  if (pathname === "/" || pathname === "/login") return null

  return (
    <aside className={`fixed top-0 left-0 h-screen bg-gray-950 border-r border-gray-800 flex flex-col transition-all duration-200 z-40 ${open ? "w-56" : "w-16"}`}>
      {/* Logo + toggle */}
      <div className={`flex items-center h-16 px-4 border-b border-gray-800 ${open ? "justify-between" : "justify-center"}`}>
        {open && (
          <Link href="/" className="text-sm font-bold text-white">
            Market<span className="text-blue-500">Pulse</span>
          </Link>
        )}
        <button onClick={toggleOpen} className="text-gray-400 hover:text-white transition-colors p-1 rounded-lg hover:bg-gray-800">
          {open ? (
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M11 19l-7-7 7-7m8 14l-7-7 7-7" />
            </svg>
          ) : (
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M13 5l7 7-7 7M5 5l7 7-7 7" />
            </svg>
          )}
        </button>
      </div>

      {/* Nav links */}
      <nav className="flex-1 overflow-y-auto py-4 px-2 space-y-0.5">
        {links.map(link => {
          const active = pathname === link.href
          return (
            <Link
              key={link.href}
              href={link.href}
              title={!open ? link.label : undefined}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-colors ${
                active
                  ? "bg-blue-600/15 text-blue-400"
                  : "text-gray-400 hover:text-white hover:bg-gray-800/60"
              } ${!open ? "justify-center" : ""}`}
            >
              <span className="flex-shrink-0">{link.icon}</span>
              {open && <span>{link.label}</span>}
            </Link>
          )
        })}
      </nav>

      {/* User sectie */}
      <div className="border-t border-gray-800 p-3">
        {user ? (
          <div className={`flex items-center gap-3 ${!open ? "justify-center" : ""}`}>
            <div className="w-8 h-8 rounded-full bg-blue-600/20 border border-blue-500/30 flex items-center justify-center flex-shrink-0">
              <span className="text-blue-400 text-xs font-bold uppercase">
                {user.email?.[0]}
              </span>
            </div>
            {open && (
              <div className="flex-1 min-w-0">
                <p className="text-xs text-gray-300 truncate">{user.email}</p>
                <button onClick={handleLogout} className="text-xs text-gray-500 hover:text-red-400 transition-colors">
                  Uitloggen
                </button>
              </div>
            )}
          </div>
        ) : (
          <Link
            href="/login"
            className={`flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium text-gray-400 hover:text-white hover:bg-gray-800/60 transition-colors ${!open ? "justify-center" : ""}`}
          >
            <svg className="w-5 h-5 flex-shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.8}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M11 16l-4-4m0 0l4-4m-4 4h14m-5 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h7a3 3 0 013 3v1" />
            </svg>
            {open && <span>Inloggen</span>}
          </Link>
        )}
      </div>
    </aside>
  )
}
